package model;

/**
 *
 * @author daw
 */
public class LineInfo {

    private String lineId;
    private String line;
    private String label;
    private String name;
    private String headerA;
    private String headerB;
    private String direction;
    private String dayType;
    private String startTime;
    private String stopTime;
    private String minimumFrequency;
    private String maximumFrequency;
    private String frequencyA;
    private String frequencyB;

    public String getLineId() {
        return lineId;
    }

    public void setLineId(String lineId) {
        this.lineId = lineId;
    }

    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHeaderA() {
        return headerA;
    }

    public void setHeaderA(String headerA) {
        this.headerA = headerA;
    }

    public String getHeaderB() {
        return headerB;
    }

    public void setHeaderB(String headerB) {
        this.headerB = headerB;
    }

    public String getDirection() {
        return direction;
    }

    public void setDirection(String direction) {
        this.direction = direction;
    }

    public String getDayType() {
        return dayType;
    }

    public void setDayType(String dayType) {
        this.dayType = dayType;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getStopTime() {
        return stopTime;
    }

    public void setStopTime(String stopTime) {
        this.stopTime = stopTime;
    }

    public String getMinimumFrequency() {
        return minimumFrequency;
    }

    public void setMinimumFrequency(String minimumFrequency) {
        this.minimumFrequency = minimumFrequency;
    }

    public String getMaximumFrequency() {
        return maximumFrequency;
    }

    public void setMaximumFrequency(String maximumFrequency) {
        this.maximumFrequency = maximumFrequency;
    }

    public String getFrequencyA() {
        return frequencyA;
    }

    public void setFrequencyA(String frequencyA) {
        this.frequencyA = frequencyA;
    }

    public String getFrequencyB() {
        return frequencyB;
    }

    public void setFrequencyB(String frequencyB) {
        this.frequencyB = frequencyB;
    }
    
    
}
